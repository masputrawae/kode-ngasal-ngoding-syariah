// Tipe Data Di JavaScript

// 1. String
let dataString = "Ini Adalah String"
console.log(dataString)
console.log(typeof dataString)

// Output: 
// Ini Adalah String
// string

// 2. Number
let dataNumber = 10
console.log(dataNumber)
console.log(typeof dataNumber)

// Output: 
// 10
// number

// 3. Boolean
let dataBoolean = true
console.log(dataBoolean)
console.log(typeof dataBoolean)

// Output: 
// true
// boolean

// 4. Undefined / Kosong
let dataKosong;
console.log(dataKosong) // undefined
console.log(typeof dataKosong) // undefined